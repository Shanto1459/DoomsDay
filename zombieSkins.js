// Queue ALL zombie sprites so they exist in ASSET_MANAGER before the game starts
window.queueZombieSkins = function (ASSET_MANAGER) {
  for (const skinKey of Object.keys(window.ZOMBIE_SKINS)) {
    const skin = window.ZOMBIE_SKINS[skinKey];
    for (const dir of ["up", "down", "left", "right"]) {
      const d = skin[dir];
      ["idle", "walk", "first", "second", "death1", "death2"].forEach((k) => {
        if (d && d[k]) ASSET_MANAGER.queueDownload(d[k]);
      });
    }
  }
};